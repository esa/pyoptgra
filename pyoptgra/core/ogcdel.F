C ======================================================================
C INP | DELCON(NUMCON+1) | R*8 | CONSTRAINTS DELTAS (1:NUMCON)
C ======================================================================
C 2008/01/16 | J. SCHOENMAEKERS | NEW
C ======================================================================
      IMPLICIT NONE
