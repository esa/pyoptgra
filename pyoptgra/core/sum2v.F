      SUBROUTINE SUM2V (V1, V2, V, K)
C ======================================================================
C V(1:K) = V1(1:K) + V2(1:K)
C ======================================================================
      IMPLICIT REAL(8)(A-H,O-Z)
      IMPLICIT INTEGER(4)(I-N)
C ======================================================================
      DIMENSION V1(*), V2(*), V(*)
C ======================================================================
      DO 1000 I = 1, K
      V(I) = V1(I) + V2(I)
 1000 CONTINUE
C ======================================================================
      RETURN
      END
