      SUBROUTINE OGWTAB (LUNTAB, LEVTAB)
C ======================================================================
C NEAR-LINEAR OPTIMISATION TOOL TAILORED FOR S/C TRAJECTORY DESIGN:
C DEFINE WRITING IN TABLE FILE
C ======================================================================
C INP | LUNTAB           | I*4 | LOGICAL UNIT FOR WRITING TABLE
C ----------------------------------------------------------------------
C INP | LEVTAB           | I*4 | LEVEL OF TAB
C     |                  |     | -> 0=NO OUTPUT
C     |                  |     | -> 1<ALL
C ======================================================================
C 2008/01/16 | J. SCHOENMAEKERS | NEW
C ======================================================================
      IMPLICIT NONE
C ======================================================================
#include <ogdata.inc>
C ======================================================================
      INTEGER(4)   LUNTAB
      INTEGER(4)   LEVTAB
C ======================================================================
      TABLUN = LUNTAB
      TABLEV = LEVTAB
C ======================================================================
      RETURN
      END
