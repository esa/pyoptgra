      SUBROUTINE OGWRIT (LEV,STR)
C ======================================================================
C 2014/07/29 | J. SCHOENMAEKERS | NEW
C ======================================================================
      IMPLICIT NONE
C ======================================================================
#include <ogdata.inc>
C ======================================================================
      CHARACTER*(*) STR
      INTEGER(4)LEV, RET, mexprintf, mexevalstring
C ======================================================================
      IF (LEV .LE. LOGLEV) THEN
      WRITE (LOGLUN,'(A)') STR
      flush (LOGLUN)
      ENDIF
C ======================================================================
      IF (LEV .LE. MATLEV) THEN
      CALL MIF_WRT_LIN (STR)
      ENDIF
C ======================================================================
      RETURN
      END
