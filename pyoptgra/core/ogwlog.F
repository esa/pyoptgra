      SUBROUTINE OGWLOG (LUNLOG, LEVLOG)
C ======================================================================
C NEAR-LINEAR OPTIMISATION TOOL TAILORED FOR S/C TRAJECTORY DESIGN:
C DEFINE WRITING IN LOG FILE
C ======================================================================
C INP | LUNLOG           | I*4 | LOGICAL UNIT FOR WRITING LOG
C ----------------------------------------------------------------------
C INP | LEVLOG           | I*4 | LEVEL OF LOG
C     |                  |     | -> 0=NO OUTPUT
C     |                  |     | -> 1<ALL
C ======================================================================
C 2008/01/16 | J. SCHOENMAEKERS | NEW
C ======================================================================
      IMPLICIT NONE
C ======================================================================
#include <ogdata.inc>
C ======================================================================
      INTEGER(4)   LUNLOG
      INTEGER(4)   LEVLOG
C ======================================================================
      LOGLUN = LUNLOG
      LOGLEV = LEVLOG
C ======================================================================
      RETURN
      END
