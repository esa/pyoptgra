      SUBROUTINE OGVTYP (TYPVAR)
C ======================================================================
C NEAR-LINEAR OPTIMISATION TOOL TAILORED FOR S/C TRAJECTORY DESIGN:
C DEFINE VARIABLE TYPE
C ======================================================================
C INP | TYPVAR(NUMVAR)   | I*4 | VARIABLES TYPE
C     |                  |     | -> 0=FREE VARIABLE
C     |                  |     | -> 1=PARAMETER FOR SENSITIVITY
C ======================================================================
C 2008/01/16 | J. SCHOENMAEKERS | NEW
C ======================================================================
      IMPLICIT NONE
C ======================================================================
#include <ogdata.inc>
C ======================================================================
      INTEGER(4)   TYPVAR(NUMVAR)
C ======================================================================
      INTEGER(4)   VAR
C ======================================================================
      DO VAR = 1, NUMVAR
         VARTYP(VAR) = TYPVAR(VAR)
      ENDDO
C ======================================================================
      RETURN
      END
